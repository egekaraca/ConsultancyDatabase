    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {
        String removedbranchCode = jTextField3.getText();
    
        if (removedbranchCode.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter the branch code.");
            return;
        }
    
        try {
            Connection connection = DriverManager.getConnection(
                    "jdbc:mysql://localhost:3306/consultancy",
                    "root",
                    "1234");
    
            Statement statement = connection.createStatement();
            String query = "DELETE FROM branch WHERE branch_code = '" + removedbranchCode + "'";
    
            int res = statement.executeUpdate(query);
    
            if (res > 0) {
                JOptionPane.showMessageDialog(this, "Branch removed successfully");
            } else {
                JOptionPane.showMessageDialog(this, "Error: Branch code not found");
            }
    
            // Refresh jTable1
            String query2 = "SELECT * FROM branch";
            ResultSet res2 = statement.executeQuery(query2);
    
            DefaultTableModel model = new DefaultTableModel();
            jTable1.setModel(model);
    
            ResultSetMetaData rsmd = res2.getMetaData();
            int columns = rsmd.getColumnCount();
    
            for (int i = 1; i <= columns; i++) {
                model.addColumn(rsmd.getColumnName(i));
            }
    
            while (res2.next()) {
                Object[] row = new Object[columns];
                for (int i = 1; i <= columns; i++) {
                    row[i-1] = res2.getObject(i);
                }
                model.addRow(row);
            }
    
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }
