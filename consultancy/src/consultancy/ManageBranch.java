    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {
    String branchCode = jTextField1.getText();
    String address = jTextField2.getText();

    if (branchCode.isEmpty() || address.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Please fill in all fields.");
        return;
    }

    try {
        Connection connection = DriverManager.getConnection(
                "jdbc:mysql://localhost:3306/consultancy",
                "root",
                "1234");

        Statement statement = connection.createStatement();
        String query = "INSERT INTO branch (branch_code, Address) VALUES ('" + branchCode + "', '" + address + "')";
        int res = statement.executeUpdate(query);

        JOptionPane.showMessageDialog(this, "Branch added successfully");

        // Display the branches in jTable1
        String query2 = "SELECT * FROM branch";
        ResultSet res2 = statement.executeQuery(query2);

        DefaultTableModel model = new DefaultTableModel();
        jTable1.setModel(model);

        ResultSetMetaData rsmd = res2.getMetaData();
        int columns = rsmd.getColumnCount();

        for (int i = 1; i <= columns; i++) {
            model.addColumn(rsmd.getColumnName(i));
        }

        while (res2.next()) {
            Object[] row = new Object[columns];
            for (int i = 1; i <= columns; i++) {
                row[i-1] = res2.getObject(i);
            }
            model.addRow(row);
        }

    } catch (SQLException e) {
        JOptionPane.showMessageDialog(this, e.getMessage());
    }
}

